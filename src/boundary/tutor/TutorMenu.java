    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String id = textID.getText().toUpperCase();
        String name = textName.getText().toUpperCase();
        String gender = (String) comboGender.getSelectedItem();
        String status = (String) comboStatus.getSelectedItem();
        String email = textEmail.getText();

        if (id.isEmpty() || name.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All text fields must be filled.");
        } else {
            if (id.length() != 5) {
                JOptionPane.showMessageDialog(this, "ID is only 5 characters.");
            } else if (!email.contains("@")) {
                JOptionPane.showMessageDialog(this, "Please enter a valid Email address.");
            } else {
                boolean add = tutorManager.addTutor(new Tutor(id, name, gender, status, email));

                if (add) {
                    JOptionPane.showMessageDialog(this, "Tutor added succesfully");
                    textID.setText("");
                    textName.setText("");
                    textEmail.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Tutor with the same tutor id existed, failed to add");
                }

            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

