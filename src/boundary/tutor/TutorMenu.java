    private void removeTutorMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeTutorMenuActionPerformed
        String removeSearch = JOptionPane.showInputDialog(this, "Please enter the tutor's ID or Name", "Delete Tutor", JOptionPane.INFORMATION_MESSAGE);
        boolean found = false;

        if (removeSearch != null) {
            Tutor tutor = TutorManager.findTutor(removeSearch);

            if (tutor != null) {
                String tutorInfo = String.format("Tutor ID: %s\n Tutor Name: %s\n Gender : %s\n Work Status: %s\n Email: %s\nAre you sure you want to delete this ?",
                        tutor.getTutorId(), tutor.getTutorName(), tutor.getTutorGender(), tutor.getTutorWork(), tutor.getTutorEmail());

                int confirm = JOptionPane.showConfirmDialog(this, tutorInfo, "Confirm", JOptionPane.YES_NO_OPTION);

                if (confirm == JOptionPane.YES_OPTION) {
                    TutorManager.removeTutor(tutor);
                    found = true;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tutor not found");
            }
        }

        if (found) {
            JOptionPane.showMessageDialog(this, "Deleted Successfully");
        }
    }//GEN-LAST:event_removeTutorMenuActionPerformed

