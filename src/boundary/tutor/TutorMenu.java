    public void addEntryToJTable(SetInterface<Tutor> tutorList, JTable table) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        if (!tutorList.isEmpty()) {
            model.setRowCount(0);
            for (Tutor tutor : tutorList) {
                Object[] rowData = new Object[5]; // Adjust the size based on the number of columns in your JTable

                // Populate rowData with tutor data
                rowData[0] = tutor.getTutorId();
                rowData[1] = tutor.getTutorName();
                rowData[2] = tutor.getTutorGender();
                rowData[3] = tutor.getTutorWork();
                rowData[4] = tutor.getTutorEmail();

                //Add the rowData to the DefaultTableModel
                model.addRow(rowData);
                repaint();
            }
        } else {
            JOptionPane.showMessageDialog(this, "There is no records");
        }
    }

