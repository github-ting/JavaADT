        rpTextArea.setText("\t\t\t         Full Time Tutors \n");
        rpTextArea.append("=========================================================================================\n");
        rpTextArea.append(String.format("\n \t\t\t\t       Total tutors: %d\n\n", sortedList.size()));
        rpTextArea.append("No      Id \t Name\t                  Gender      \t  Email\n");
