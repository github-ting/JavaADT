    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (jtfCourseCode.getText().length() != 0 && jtfCourseName.getText().length() != 0) {
            int result = JOptionPane.showConfirmDialog(null, "Are you sure ?", "Delete", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                if (ManageCourse.removeCourse(jtfCourseCode.getText())) {
                    JOptionPane.showMessageDialog(null, "Successfully deteled the course.", "Successful", JOptionPane.INFORMATION_MESSAGE);
                    jtfCourseCode.setText("");
                    jtfCourseName.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Cannot find course with given course code.", "Error", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter the course code. ", "No input", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

