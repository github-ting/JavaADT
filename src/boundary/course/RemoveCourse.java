    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (jtfCourseCode.getText().length() != 0) {
            Course course = ManageCourse.findCourse(jtfCourseCode.getText());
            if (course == null) {
                JOptionPane.showMessageDialog(null, "This course doesn't exist", "Error", JOptionPane.INFORMATION_MESSAGE);
            } else {
                jtfCourseName.setText(course.getCourseName());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please enter a course code.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_okButtonActionPerformed

